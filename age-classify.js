let age = 25;
if (age >= 60) {
  console.log(`${age}: Senior Citizen`);
} else if (age >= 36) {
  console.log(`${age}: Adult`);
} else if (age >= 18) {
  console.log(`${age}: Young Adult`);
} else if (age >= 13) {
  console.log(`${age}: Teenager`);
} else if (age >= 3) {
  console.log(`${age}: Child`);
} else if (age >= 0) {
  console.log(`${age}: Adult`);
} else {
  console.log(`${age}: Invalid Age Range`);
}
